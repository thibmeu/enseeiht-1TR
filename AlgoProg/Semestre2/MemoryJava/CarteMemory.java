/** Définition des cartes d'un jeu de 52 cartes.
  *
  * @author	Xavier Crégut <Prenom.Nom@enseeiht.fr>
  */
public class CarteMemory<F, V> {

	private F famille;
	private V valeur;

	public CarteMemory(F famille, V valeur) {
		this.famille = famille;
		this.valeur = valeur;
	}

	public F getFamille() {
		return this.famille;
	}

	public V getValeur() {
		return this.valeur;
	}

	@Override public String toString() {
		return "" + this.getValeur() + " " + this.getFamille();
	}

}
